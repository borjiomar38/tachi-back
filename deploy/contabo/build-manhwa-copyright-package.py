def parse_args() -> argparse.Namespace:
  parser = argparse.ArgumentParser(
    description='Prepare a private copyright package for a Nayovi manhwa.'
  )
  parser.add_argument('--series-slug', required=True)
  parser.add_argument('--chapter-number', type=int, default=0)
  parser.add_argument(
    '--scope',
    choices=['auto', 'series', 'chapter'],
    default='auto',
    help='auto uses chapter scope when --chapter-number is provided.',
  )
  parser.add_argument('--package-file', default='')
  parser.add_argument('--context-root', default=str(DEFAULT_CONTEXT_ROOT))
  parser.add_argument('--generated-root', default=str(DEFAULT_GENERATED_ROOT))
  parser.add_argument('--output-root', default=str(DEFAULT_OUTPUT_ROOT))
  parser.add_argument('--private-root', default=str(DEFAULT_PRIVATE_ROOT))
  parser.add_argument(
    '--allow-incomplete',
    action='store_true',
    help='Create the package even when chapter panels are missing. Filing remains blocked.',
  )
  parser.add_argument(
    '--no-queue',
    action='store_true',
    help='Do not append the generated package to the private admin action queue.',
  )
  parser.add_argument(
    '--paid-filing-intent',
    action='store_true',
    help='Mark the draft as intended for official paid filing. Default is free evidence only.',
  )
  return parser.parse_args()


def coerce_dict(value: Any) -> dict[str, Any]:
  return value if isinstance(value, dict) else {}


def coerce_list(value: Any) -> list[Any]:
  return value if isinstance(value, list) else []


def read_json(path: pathlib.Path) -> dict[str, Any]:
  value = json.loads(path.read_text(encoding='utf-8'))
  if not isinstance(value, dict):
    raise ValueError(f'{path}: expected a JSON object')
  return value


def write_json(path: pathlib.Path, value: dict[str, Any]) -> None:
  path.parent.mkdir(parents=True, exist_ok=True)
  path.write_text(json.dumps(value, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')


def utc_now() -> dt.datetime:
  return dt.datetime.now(dt.UTC)


def timestamp_token(now: dt.datetime) -> str:
  return now.strftime('%Y%m%dT%H%M%SZ')


def slugify(value: str, fallback: str) -> str:
  slug = re.sub(r'[^a-zA-Z0-9]+', '-', value.lower()).strip('-')
  return slug or fallback


def sha256_file(path: pathlib.Path) -> str:
  digest = hashlib.sha256()
  with path.open('rb') as handle:
    for chunk in iter(lambda: handle.read(1024 * 1024), b''):
      digest.update(chunk)
  return digest.hexdigest()


def git_commit() -> str:
  try:
    return subprocess.check_output(
      ['git', 'rev-parse', 'HEAD'],
      stderr=subprocess.DEVNULL,
      text=True,
    ).strip()
  except (OSError, subprocess.CalledProcessError):
    return 'unknown'


def git_status_short() -> str:
  try:
    return subprocess.check_output(
      ['git', 'status', '--short'],
      stderr=subprocess.DEVNULL,
      text=True,
    ).strip()
  except (OSError, subprocess.CalledProcessError):
    return 'unavailable'


def chapter_package_path(
  *,
  generated_root: pathlib.Path,
  series_slug: str,
  chapter_number: int,
  explicit_path: str,
) -> pathlib.Path:
  if explicit_path:
    return pathlib.Path(explicit_path)
  return generated_root / f'{series_slug}-chapter-{chapter_number:03d}.json'


def image_manifest_path(
  *,
  private_root: pathlib.Path,
  series_slug: str,
  chapter_number: int,
) -> pathlib.Path:
  return (
    private_root
    / series_slug
    / f'chapter-{chapter_number:03d}'
    / f'chapter-{chapter_number:03d}-images.json'
  )


def local_panel_path(
  *,
  private_root: pathlib.Path,
  series_slug: str,
  chapter_number: int,
  panel_number: int,
) -> pathlib.Path:
  return (
    private_root
    / series_slug
    / f'chapter-{chapter_number:03d}'
    / f'panel-{panel_number:03d}.png'
  )


def context_dir(context_root: pathlib.Path, series_slug: str) -> pathlib.Path:
  return context_root / series_slug


def copy_file(source: pathlib.Path, destination: pathlib.Path) -> None:
  if not source.exists() or not source.is_file():
    return
  destination.parent.mkdir(parents=True, exist_ok=True)
  shutil.copy2(source, destination)


def zip_dir(source_dir: pathlib.Path, zip_path: pathlib.Path) -> None:
  zip_path.parent.mkdir(parents=True, exist_ok=True)
  with zipfile.ZipFile(zip_path, 'w', compression=zipfile.ZIP_DEFLATED) as archive:
    for path in sorted(source_dir.rglob('*')):
      if path.is_file():
        archive.write(path, path.relative_to(source_dir))


def collect_hashes(root: pathlib.Path) -> list[dict[str, str]]:
  entries: list[dict[str, str]] = []
  for path in sorted(root.rglob('*')):
    if path.is_file():
      entries.append(
        {
          'sha256': sha256_file(path),
          'path': str(path.relative_to(root)),
          'bytes': str(path.stat().st_size),
        }
      )
  return entries


def write_hash_file(path: pathlib.Path, hashes: list[dict[str, str]]) -> None:
  lines = [f'{entry["sha256"]}  {entry["path"]}' for entry in hashes]
  path.write_text('\n'.join(lines) + '\n', encoding='utf-8')


def safe_text(value: Any) -> str:
  return str(value or '').strip()


def series_from_package(package: dict[str, Any], fallback_slug: str) -> dict[str, Any]:
  series = coerce_dict(package.get('series'))
  if not series:
    series = {'slug': fallback_slug, 'title': fallback_slug.replace('-', ' ').title()}
  return series


def chapter_from_package(package: dict[str, Any], chapter_number: int) -> dict[str, Any]:
  chapter = coerce_dict(package.get('chapter'))
  if not chapter:
    chapter = {'chapter_number': chapter_number, 'title': f'Chapter {chapter_number}'}
  return chapter


def rendered_panel_numbers(manifest: dict[str, Any]) -> set[int]:
  numbers: set[int] = set()
  for item in coerce_list(manifest.get('rendered')):
    panel_number = int(coerce_dict(item).get('panel_number') or 0)
    if panel_number > 0:
      numbers.add(panel_number)
  return numbers


def build_chapter_work_html(
  *,
  package: dict[str, Any],
  series: dict[str, Any],
  chapter: dict[str, Any],
  copied_panels: dict[int, pathlib.Path],
) -> str:
  title = safe_text(series.get('title'))
  chapter_title = safe_text(chapter.get('title'))
  scenario = safe_text(chapter.get('full_text_scenario'))
  panels_html: list[str] = []

  for raw_panel in coerce_list(package.get('panels')):
    panel = coerce_dict(raw_panel)
    panel_number = int(panel.get('panel_number') or 0)
    narration = safe_text(panel.get('narration'))
    dialogue = [safe_text(line) for line in coerce_list(panel.get('dialogue')) if safe_text(line)]
    image_path = copied_panels.get(panel_number)
    image_html = ''
    if image_path:
      image_html = (
        f'<img src="{html.escape(str(image_path))}" alt="Panel {panel_number}" />'
      )
    dialogue_html = ''.join(f'<li>{html.escape(line)}</li>' for line in dialogue)
    panels_html.append(
      '\n'.join(
        [
          '<section class="panel">',
          f'<h2>Panel {panel_number}</h2>',
          image_html,
          f'<p><strong>Narration:</strong> {html.escape(narration)}</p>' if narration else '',
          f'<ul>{dialogue_html}</ul>' if dialogue_html else '',
          '</section>',
        ]
      )
    )

  return '\n'.join(
    [
      '<!doctype html>',
      '<html lang="en">',
      '<head>',
      '<meta charset="utf-8" />',
      f'<title>{html.escape(title)} - {html.escape(chapter_title)}</title>',
      '<style>',
      'body{margin:0;background:#0b0b10;color:#f7f2ea;font-family:Georgia,serif;}',
      'main{max-width:960px;margin:0 auto;padding:32px 18px;}',
      'img{display:block;width:100%;height:auto;margin:18px 0;border:1px solid #2d2935;}',
      '.panel{margin:0 0 42px;}',
      'p,li{font-size:18px;line-height:1.65;}',
      '</style>',
      '</head>',
      '<body><main>',
      f'<h1>{html.escape(title)}</h1>',
      f'<h2>Chapter {html.escape(str(chapter.get("chapter_number") or ""))}: {html.escape(chapter_title)}</h2>',
      f'<p>{html.escape(scenario)}</p>' if scenario else '',
      *panels_html,
      '</main></body></html>',
    ]
  )


def build_series_work_html(
  *,
  series_bible: dict[str, Any],
  characters: dict[str, Any],
  series_slug: str,
) -> str:
  title = safe_text(series_bible.get('title')) or series_slug.replace('-', ' ').title()
  premise = safe_text(series_bible.get('full_premise'))
  logline = safe_text(series_bible.get('logline'))
  character_items: list[str] = []

  for raw_character in coerce_list(characters.get('characters')):
    character = coerce_dict(raw_character)
    character_items.append(
      '<li>'
      f'<strong>{html.escape(safe_text(character.get("name")) or safe_text(character.get("id")))}</strong>: '
      f'{html.escape(safe_text(character.get("role")))}'
      '</li>'
    )

  return '\n'.join(
    [
      '<!doctype html>',
      '<html lang="en">',
      '<head><meta charset="utf-8" />',
      f'<title>{html.escape(title)} - Series Bible</title>',
      '<style>body{max-width:900px;margin:0 auto;padding:40px 20px;font-family:Georgia,serif;line-height:1.65;}</style>',
      '</head><body>',
      f'<h1>{html.escape(title)}</h1>',
      f'<p><strong>Logline:</strong> {html.escape(logline)}</p>' if logline else '',
      f'<p>{html.escape(premise)}</p>' if premise else '',
      '<h2>Character Register</h2>',
      f'<ul>{"".join(character_items)}</ul>' if character_items else '<p>No character register found.</p>',
      '</body></html>',
    ]
  )


