def build_chapter_package(args: argparse.Namespace, now: dt.datetime) -> dict[str, Any]:
  series_slug = args.series_slug
  chapter_number = int(args.chapter_number)
  package_path = chapter_package_path(
    generated_root=pathlib.Path(args.generated_root),
    series_slug=series_slug,
    chapter_number=chapter_number,
    explicit_path=args.package_file,
  )
  if not package_path.exists():
    raise SystemExit(f'Chapter package is missing: {package_path}')

  package = read_json(package_path)
  series = series_from_package(package, series_slug)
  chapter = chapter_from_package(package, chapter_number)
  panels = [coerce_dict(panel) for panel in coerce_list(package.get('panels'))]
  planned_numbers = {
    int(panel.get('panel_number') or 0)
    for panel in panels
    if int(panel.get('panel_number') or 0) > 0
  }
  manifest_path = image_manifest_path(
    private_root=pathlib.Path(args.private_root),
    series_slug=series_slug,
    chapter_number=chapter_number,
  )
  manifest = read_json(manifest_path) if manifest_path.exists() else {}
  rendered_numbers = rendered_panel_numbers(manifest)
  existing_numbers: set[int] = set()
  copied_panels: dict[int, pathlib.Path] = {}

  output_dir = (
    pathlib.Path(args.output_root)
    / series_slug
    / f'chapter-{chapter_number:03d}'
    / timestamp_token(now)
  )
  work_dir = output_dir / 'work-deposit'
  evidence_dir = output_dir / 'evidence'
  panel_dir = work_dir / 'panels'

  for panel_number in sorted(planned_numbers):
    source = local_panel_path(
      private_root=pathlib.Path(args.private_root),
      series_slug=series_slug,
      chapter_number=chapter_number,
      panel_number=panel_number,
    )
    if source.exists():
      existing_numbers.add(panel_number)
      destination = panel_dir / source.name
      copy_file(source, destination)
      copied_panels[panel_number] = pathlib.Path('panels') / source.name

  missing_numbers = sorted(planned_numbers - existing_numbers)
  status = safe_text(package.get('publication_status'))
  reasons_not_ready: list[str] = []
  if missing_numbers:
    reasons_not_ready.append(
      f'Missing rendered chapter panels: {", ".join(str(number) for number in missing_numbers)}'
    )
  if status not in {'approved_by_expert_ai', 'published'}:
    reasons_not_ready.append(f'Chapter package status is not approved/published: {status or "missing"}')
  if not panels:
    reasons_not_ready.append('No panel script exists in the chapter package.')
  if reasons_not_ready and not args.allow_incomplete:
    raise SystemExit(
      'Chapter is not complete enough for copyright packaging. '
      'Use --allow-incomplete to create a private evidence snapshot.'
    )

  work_html = build_chapter_work_html(
    package=package,
    series=series,
    chapter=chapter,
    copied_panels=copied_panels,
  )
  work_dir.mkdir(parents=True, exist_ok=True)
  (work_dir / 'work.html').write_text(work_html, encoding='utf-8')
  write_json(
    work_dir / 'metadata.json',
    {
      'series': series,
      'chapter': chapter,
      'series_slug': series_slug,
      'scope': 'chapter',
      'generated_at': now.isoformat().replace('+00:00', 'Z'),
      'source_package': str(package_path),
      'image_manifest': str(manifest_path) if manifest_path.exists() else None,
      'planned_panel_count': len(planned_numbers),
      'rendered_panel_count': len(existing_numbers),
      'missing_panel_numbers': missing_numbers,
      'copyright_notice': 'Copyright ownership and publication status must be confirmed by an authorized Nayovi admin before filing.',
    },
  )

  copy_file(package_path, evidence_dir / 'source' / package_path.name)
  copy_file(manifest_path, evidence_dir / 'source' / manifest_path.name)
  context_path = context_dir(pathlib.Path(args.context_root), series_slug)
  if context_path.exists():
    for source in sorted(context_path.rglob('*.json')):
      copy_file(source, evidence_dir / 'context' / source.relative_to(context_path))

  title = f'{safe_text(series.get("title"))} - Chapter {chapter.get("chapter_number")}: {safe_text(chapter.get("title"))}'
  filing_draft = filing_draft_common(
    scope='chapter',
    title=title,
    complete=not missing_numbers and bool(panels),
    reasons_not_ready=reasons_not_ready,
    paid_filing_intent=bool(args.paid_filing_intent),
  )
  filing_draft.update(
    {
      'chapter_number': chapter_number,
      'publication_status': status,
      'deposit_recommendation': 'Use work-deposit.zip for the chapter deposit after admin review.',
      'internal_evidence_recommendation': 'Keep evidence.zip private for provenance and originality records; do not upload prompt evidence unless legally advised.',
    }
  )
  write_json(output_dir / 'usco-application-draft.json', filing_draft)
  write_checklist(output_dir / 'filing-checklist.md', filing_draft)

  return finalize_package(
    output_dir=output_dir,
    work_dir=work_dir,
    evidence_dir=evidence_dir,
    now=now,
    scope='chapter',
    series_slug=series_slug,
    chapter_number=chapter_number,
    filing_draft=filing_draft,
    source_paths=[package_path, manifest_path],
    append_queue=not args.no_queue,
  )


def build_series_package(args: argparse.Namespace, now: dt.datetime) -> dict[str, Any]:
  series_slug = args.series_slug
  context_path = context_dir(pathlib.Path(args.context_root), series_slug)
  if not context_path.exists():
    raise SystemExit(f'Series context directory is missing: {context_path}')

  series_bible_path = context_path / 'series-bible.json'
  characters_path = context_path / 'characters.json'
  series_bible = read_json(series_bible_path) if series_bible_path.exists() else {}
  characters = read_json(characters_path) if characters_path.exists() else {}
  title = safe_text(series_bible.get('title')) or series_slug.replace('-', ' ').title()
  reasons_not_ready: list[str] = []
  if not series_bible:
    reasons_not_ready.append('Missing series-bible.json.')
  if not coerce_list(characters.get('characters')):
    reasons_not_ready.append('Missing character register.')
  if reasons_not_ready and not args.allow_incomplete:
    raise SystemExit(
      'Series bible is not complete enough for copyright packaging. '
      'Use --allow-incomplete to create a private evidence snapshot.'
    )

  output_dir = pathlib.Path(args.output_root) / series_slug / 'series-bible' / timestamp_token(now)
  work_dir = output_dir / 'work-deposit'
  evidence_dir = output_dir / 'evidence'
  work_dir.mkdir(parents=True, exist_ok=True)
  (work_dir / 'work.html').write_text(
    build_series_work_html(
      series_bible=series_bible,
      characters=characters,
      series_slug=series_slug,
    ),
    encoding='utf-8',
  )
  write_json(
    work_dir / 'metadata.json',
    {
      'series_slug': series_slug,
      'scope': 'series-bible',
      'title': title,
      'generated_at': now.isoformat().replace('+00:00', 'Z'),
      'source_context': str(context_path),
      'coverage_warning': 'This package covers only the fixed series bible/context expression included here, not future unwritten chapters.',
    },
  )

  for source in sorted(context_path.rglob('*.json')):
    copy_file(source, evidence_dir / 'context' / source.relative_to(context_path))

  filing_draft = filing_draft_common(
    scope='series-bible',
    title=f'{title} - Series Bible',
    complete=not reasons_not_ready,
    reasons_not_ready=reasons_not_ready,
    paid_filing_intent=bool(args.paid_filing_intent),
  )
  filing_draft.update(
    {
      'publication_status': 'unpublished_internal_series_bible',
      'deposit_recommendation': 'Use work-deposit.zip only if registering the fixed story bible/treatment. Register chapters separately when their art/dialogue is complete.',
      'internal_evidence_recommendation': 'Keep evidence.zip private for provenance and originality records.',
    }
  )
  write_json(output_dir / 'usco-application-draft.json', filing_draft)
  write_checklist(output_dir / 'filing-checklist.md', filing_draft)

  return finalize_package(
    output_dir=output_dir,
    work_dir=work_dir,
    evidence_dir=evidence_dir,
    now=now,
    scope='series-bible',
    series_slug=series_slug,
    chapter_number=0,
    filing_draft=filing_draft,
    source_paths=[series_bible_path, characters_path],
    append_queue=not args.no_queue,
  )


def write_checklist(path: pathlib.Path, filing_draft: dict[str, Any]) -> None:
  lines = [
    '# Copyright Filing Checklist',
    '',
    f'- Scope: {filing_draft["scope"]}',
    f'- Title: {filing_draft["title"]}',
    f'- Filing ready by automation checks: {str(filing_draft["filing_ready"]).lower()}',
    f'- Free evidence mode: {str(filing_draft.get("free_evidence_mode", True)).lower()}',
    '- Requires admin final submission: true',
    '',
    '## Blocking Reasons',
  ]
  reasons = coerce_list(filing_draft.get('reasons_not_ready'))
  lines.extend(f'- {reason}' for reason in reasons) if reasons else lines.append('- None.')
  lines.extend(
    [
      '',
      '## Admin Steps',
      *[f'- {step}' for step in coerce_list(filing_draft.get('admin_steps'))],
      '',
      '## Official Sources To Review',
      *[f'- {source["title"]}: {source["url"]}' for source in OFFICIAL_SOURCES],
      '',
      '## Legal Boundary',
      'This automated package prepares evidence and a draft only. It does not submit, pay, or certify a U.S. Copyright Office application.',
      '',
    ]
  )
  path.write_text('\n'.join(lines), encoding='utf-8')


def finalize_package(
  *,
  output_dir: pathlib.Path,
  work_dir: pathlib.Path,
  evidence_dir: pathlib.Path,
  now: dt.datetime,
  scope: str,
  series_slug: str,
  chapter_number: int,
  filing_draft: dict[str, Any],
  source_paths: list[pathlib.Path],
  append_queue: bool,
) -> dict[str, Any]:
  evidence_dir.mkdir(parents=True, exist_ok=True)
  content_hashes = collect_hashes(work_dir) + collect_hashes(evidence_dir)
  write_hash_file(evidence_dir / 'hashes.sha256', content_hashes)

  evidence_manifest = {
    'generated_at': now.isoformat().replace('+00:00', 'Z'),
    'git_commit': git_commit(),
    'git_status_short': git_status_short(),
    'scope': scope,
    'series_slug': series_slug,
    'chapter_number': chapter_number or None,
    'source_paths': [str(path) for path in source_paths if path.exists()],
    'content_hash_count': len(content_hashes),
    'official_sources': OFFICIAL_SOURCES,
  }
  write_json(evidence_dir / 'evidence-manifest.json', evidence_manifest)

  work_zip = output_dir / 'work-deposit.zip'
  evidence_zip = output_dir / 'evidence.zip'
  zip_dir(work_dir, work_zip)
  zip_dir(evidence_dir, evidence_zip)

  summary = {
    'generated_at': now.isoformat().replace('+00:00', 'Z'),
    'scope': scope,
    'series_slug': series_slug,
    'chapter_number': chapter_number or None,
    'output_dir': str(output_dir),
    'filing_ready': bool(filing_draft.get('filing_ready')),
    'requires_admin_final_submission': True,
    'work_deposit_zip': str(work_zip),
    'work_deposit_sha256': sha256_file(work_zip),
    'evidence_zip': str(evidence_zip),
    'evidence_sha256': sha256_file(evidence_zip),
    'usco_application_draft': str(output_dir / 'usco-application-draft.json'),
    'filing_checklist': str(output_dir / 'filing-checklist.md'),
    'legal_boundary': 'Automated preparation only; official filing requires authorized admin review, payment, and certification.',
  }
  write_json(output_dir / 'package-summary.json', summary)

  if append_queue:
    queue_path = output_dir.parents[2] / 'copyright-action-queue.jsonl'
    queue_path.parent.mkdir(parents=True, exist_ok=True)
    with queue_path.open('a', encoding='utf-8') as handle:
      handle.write(json.dumps(summary, ensure_ascii=False) + '\n')

  return summary


def main() -> int:
  args = parse_args()
  scope = args.scope
  if scope == 'auto':
    scope = 'chapter' if int(args.chapter_number) > 0 else 'series'
  if scope == 'chapter' and int(args.chapter_number) <= 0:
    raise SystemExit('--chapter-number is required when scope=chapter')

  now = utc_now()
  if scope == 'chapter':
    summary = build_chapter_package(args, now)
  else:
    summary = build_series_package(args, now)

  print(json.dumps(summary, ensure_ascii=False, indent=2))
  return 0


if __name__ == '__main__':
  raise SystemExit(main())
